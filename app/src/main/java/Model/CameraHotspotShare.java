package Model;

public class CameraHotspotShare {

    private String content_type;
    private String user_id;
    private String media_link;
    private String caption;
    private String url;
    private String hotspot_content_type;
    private String hotspot_id;

    public CameraHotspotShare() {
    }

    public String getContent_type() {
        return content_type;
    }

    public void setContent_type(String content_type) {
        this.content_type = content_type;
    }

    public String getUser_id() {
        return user_id;
    }

    public void setUser_id(String user_id) {
        this.user_id = user_id;
    }

    public String getMedia_link() {
        return media_link;
    }

    public void setMedia_link(String media_link) {
        this.media_link = media_link;
    }

    public String getCaption() {
        return caption;
    }

    public void setCaption(String caption) {
        this.caption = caption;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getHotspot_content_type() {
        return hotspot_content_type;
    }

    public void setHotspot_content_type(String hotspot_content_type) {
        this.hotspot_content_type = hotspot_content_type;
    }

    public String getHotspot_id() {
        return hotspot_id;
    }

    public void setHotspot_id(String hotspot_id) {
        this.hotspot_id = hotspot_id;
    }
}
